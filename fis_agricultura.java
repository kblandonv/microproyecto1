    private void btnConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaActionPerformed
        // TODO add your handling code here:
        double Humedad_del_suelo = Double.parseDouble(TextHumedad.getText());
        double Temperatura = Double.parseDouble(TextTemperatura.getText());
        double Indice_de_Nutrientes_del_Suelo = Double.parseDouble(TextNutrientes.getText());
        
        String[] resultadosArray = resultados.evaluar(Humedad_del_suelo, Temperatura, Indice_de_Nutrientes_del_Suelo);
        
        StringBuilder resultadoFinal = new StringBuilder();
        
        for (String linea : resultadosArray) {
            resultadoFinal.append(linea).append("\n");
        }
        TextAreaCantidad.setText(resultadoFinal.toString());
           
    }//GEN-LAST:event_btnConsultaActionPerformed

